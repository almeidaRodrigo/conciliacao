package email;

public class Contact {

	/**
	 * @param email
	 */
	public Contact(String email) {
		this.email = email;
	}

	private String email;

	/**
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public void setEmail(String email) {
		this.email = email;
	}


	

}
